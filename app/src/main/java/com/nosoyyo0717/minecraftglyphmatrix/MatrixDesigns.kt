fun setDot(x: Int, y: Int, matrix: IntArray, brightness: Int = 255) {
    val index = (y * 25) + x
    if (index in 0..624) {
        matrix[index] = brightness
    }
}

fun invertMatrix(originalMatrix: IntArray): IntArray {
    val invertedMatrix = IntArray(625) { 0 }
    for (row in 0 until 25) {
        val activeCount = PHONE_3_ACTIVE_WIDTHS[row]
        val emptySpaces = (25 - activeCount) / 2
        for (col in 0 until 25) {
            if (col in emptySpaces until (emptySpaces + activeCount)) {
                val index = row * 25 + col
                invertedMatrix[index] = 255 - originalMatrix[index]
            }
        }
    }
    return invertedMatrix
}

fun boostMatrix(originalMatrix: IntArray, boostAmount: Int): IntArray {
    val boostedMatrix = IntArray(625) { 0 }
    for (i in 0..624) {
        val currentBrightness = originalMatrix[i]
        if (currentBrightness > 0) {
            boostedMatrix[i] = (currentBrightness + boostAmount).coerceIn(0, 255)
        } else {
            boostedMatrix[i] = 0
        }
    }
    return boostedMatrix
}

